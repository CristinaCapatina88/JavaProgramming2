/*
Create a class called StudentInfo, and declare variables for the followings:
                1. student name
                2. age
                3. gender
                4. school name
                5. phone number
                6. full time
 */
public class StudentInfo {
    public static void main(String[] args) {
        String student_name= "Jimmy";
        int age = 25;
        char gender ='M';
        String school_name = "Cydeo";
        String phone_number = "(852)-758-225-785";
        boolean full_time = true;
        double GPA = 2.5;

        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("school_name = " + school_name);
        System.out.println("phone_number = " + phone_number);
        System.out.println("full_time = " + full_time);
        System.out.println("GPA = " + GPA);
    }














}
